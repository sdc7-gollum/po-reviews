CREATE SCHEMA stays AUTHORIZATION zdeckert;

CREATE TABLE IF NOT EXISTS reviews
(
  stay_id  NUMERIC (12,4) NOT NULL,
  name VARCHAR(50),
  pic VARCHAR(255),
  date DATE,
  body VARCHAR,
  r_checking SMALLINT,
  r_accuracy SMALLINT,
  r_location SMALLINT,
  r_communication SMALLINT,
  r_cleanliness SMALLINT,
  r_value SMALLINT
) PARTITION BY RANGE (stay_id);

